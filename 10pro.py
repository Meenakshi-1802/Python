#arithmatic operators
a = 5
b = 2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)#remainder
print(a ** b)#a^b

#Relational / Comparison operator
a = 50
b = 20
print(a == b)#False
print(a != b)#True
print(a >= b)#True
print(a > b)#True
print(a <= b)#False
print(a < b)#False

#Assignment operator
num = 10
num = num + 10 #10+10 => 20
print(num)

#logical operator
print(not False)
print(not True)
val1 = True
val2 = True
print("AND operator:" , val1 and val2)
print("OR operator:" , val1 or val2)




